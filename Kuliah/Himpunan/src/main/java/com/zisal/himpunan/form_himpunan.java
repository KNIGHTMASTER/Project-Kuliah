    private void txtinputAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtinputAActionPerformed
    String ldata=JOptionPane.showInputDialog(null,"Masukkan panjang data himpunan A",JOptionPane.QUESTION_MESSAGE);
    String isidataA,hasildataA="";
    totala=Integer.parseInt(ldata);
    if(totala>15){
        JOptionPane.showMessageDialog(rootPane, "total data harus < 15");
        System.exit(totala);
    }
 else{
    for(b=0;b<=totala-1;b++){
        isidataA=JOptionPane.showInputDialog(null,"INDEX["+(b+1)+"]",JOptionPane.QUESTION_MESSAGE);
        himA[b]=isidataA.charAt(0);
        hasildataA+=String.valueOf(himA[b])+" ";
        txthimA.setText("{ "+hasildataA+" }");
    }
        }

    }//GEN-LAST:event_txtinputAActionPerformed

