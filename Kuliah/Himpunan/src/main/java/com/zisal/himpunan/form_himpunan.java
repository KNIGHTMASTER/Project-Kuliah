    void proses(){
        if(txthimA.getText()!="" && txthimB.getText()!=""){
            if(totala!=totalb){
                JOptionPane.showMessageDialog(rootPane, "panjang data himpunan A != panjang data himpunan B !!!");
            }
 else{
                intersection();
                difAB();
                difBA();
                union();
 }
        }
    }
    void intersection(){        
                for(a=0;a<=totala-1;a++){
                    for(b=0;b<=totala-1;b++){
                        if(himA[a]==himB[b]){
                        inter[a]=himA[a];
                        }
 else if(himA[a]!=himB[b]){
   beda[a]=himA[a];
 }
                    }
                    intersection+=String.valueOf(inter[a])+" ";
                    intersection=intersection.trim();
                    txtinter.setText("{ "+intersection+" }");                    
                }                                    
    }
    void difAB(){        
        for(a=0;a<=totala-1;a++){            
              if(himA[a]!=himB[a]){
                 difAB[a]=himA[a];
                 for(b=0;b<=totala-1;b++){
                  if(himA[a]==himB[b]){
                     difAB[a]=' ';
                 }
                 }
            }
            SdifAB+=String.valueOf(difAB[a])+" ";
            SdifAB=SdifAB.trim();
            txtdifAB.setText("{ "+SdifAB+" }");
        }
    }
    void difBA(){        
        for(a=0;a<=totala-1;a++){            
                if(himB[a]!=himA[a]){
                    difBA[a]=himB[a];
                    for(b=0;b<=totala-1;b++){
                        if(himB[a]==himA[b]){
                            difBA[a]=' ';
                        }
                    }
                }   
            SdifBA+=String.valueOf(difBA[a])+" ";
            SdifBA=SdifBA.trim();
            txtdifBA.setText("{ "+SdifBA+" }");
        }
    }
    void union(){
    String union="";
    union+=intersection+SdifAB+SdifBA;
    union=union.trim();
    txtunion.setText("{ "+union+" }");
    }
